// Helper function to get a flat list of states with abbreviations
export const getStates = () => {
  return usaStatesAndCitiesData.states.map(state => ({
    name: state.name,
    abbreviation: state.abbreviation
  }));
};

// Helper function to get cities by state abbreviation
export const getCitiesByStateCode = (stateAbbreviation: string) => {
  const state = usaStatesAndCitiesData.states.find(
    state => state.abbreviation === stateAbbreviation
  );
  
  return state ? state.cities : [];
};

// Export a flat array of all cities with their state info
export const getAllCities = () => {
  return usaStatesAndCitiesData.states.flatMap(state => 
    state.cities.map(city => ({
      ...city,
      state: state.name,
      stateAbbreviation: state.abbreviation
    }))
  );
};

// Function to filter listings based on search criteria
export const getFilteredListings = (filters: any) => {
  let results = [...demoListings];
  
  // Text search
  if (filters.query) {
    const query = filters.query.toLowerCase();
    results = results.filter(
      listing =>
        listing.title.toLowerCase().includes(query) ||
        listing.description.toLowerCase().includes(query) ||
        listing.location.toLowerCase().includes(query)
    );
  }
  
  // State filter
  if (filters.state) {
    results = results.filter(listing => listing.state === filters.state);
  }
  
  // City filter
  if (filters.city) {
    results = results.filter(listing => listing.city === filters.city);
  }
  
  // Ethnicity filter
  if (filters.ethnicity && filters.ethnicity.length) {
    results = results.filter(listing => filters.ethnicity.includes(listing.ethnicity));
  }
  
  // Nationality filter
  if (filters.nationality && filters.nationality.length) {
    results = results.filter(listing => filters.nationality.includes(listing.nationality));
  }
  
  // Body type filter
  if (filters.bodyType && filters.bodyType.length) {
    results = results.filter(listing => filters.bodyType.includes(listing.bodyType));
  }
  
  // Breast type filter
  if (filters.breastType && filters.breastType.length) {
    results = results.filter(listing => filters.breastType.includes(listing.breastType));
  }
  
  // Hair color filter
  if (filters.hairColor && filters.hairColor.length) {
    results = results.filter(listing => filters.hairColor.includes(listing.hairColor));
  }
  
  // Age range filter
  if (filters.minAge && filters.maxAge) {
    results = results.filter(listing => listing.age >= filters.minAge && listing.age <= filters.maxAge);
  }
  
  // Price range filter
  if (filters.minPrice && filters.maxPrice) {
    results = results.filter(listing => listing.price >= filters.minPrice && listing.price <= filters.maxPrice);
  }
  
  // Services filter
  if (filters.services && filters.services.length) {
    results = results.filter(listing => 
      filters.services.some((service: string) => listing.services.includes(service))
    );
  }
  
  // Caters to filter
  if (filters.catersTo && filters.catersTo.length) {
    results = results.filter(listing => 
      filters.catersTo.some((caterTo: string) => listing.catersTo.includes(caterTo))
    );
  }
  
  // Place of service filter
  if (filters.placeOfService && filters.placeOfService.length) {
    results = results.filter(listing => 
      filters.placeOfService.some((place: string) => listing.placeOfService.includes(place))
    );
  }
  
  return results;
}; 